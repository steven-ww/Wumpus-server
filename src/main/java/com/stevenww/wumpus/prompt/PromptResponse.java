package com.stevenww.wumpus.prompt;

public class PromptResponse {
    private String prompt;

    public PromptResponse() {
    }

    public PromptResponse(String prompt) {
        this.prompt = prompt;
    }

    public String getPrompt() {
        return prompt;
    }

    public void setPrompt(String prompt) {
        this.prompt = prompt;
    }
}

